<br>The Lakeshore Community Chorus was born in early 2010 from the joint musical
passion of directors Tim Heck and Nancy Plantinga. Their desire to offer a
musical experience to singers as well as the surrounding communities produced
24 excited members. Today, our 40+ members represent nine lake shore
communities!<br><br>

The ensemble rehearses weekly from April through December. Many members
are busy and often travel, so two “sessions” every year, April-July and late
August-early December, were created to allow singers the opportunity to be
involved when they can. Auditions for part placement begin and a formal concert concludes
each session. Rehearsals are Thursdays from 6:30-8:30pm at the Douglas Congregational United
Church of Christ.<br><br>

In 2011, we established ourselves as a registered nonprofit organization with a Board of
Directors, and we keep growing… because our members, supporters, and communities believe
music brings joy to everyone. We are blessed!<br><br>

